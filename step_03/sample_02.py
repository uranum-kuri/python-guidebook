print("+12 =", +12)  # 単項プラス
print("-12 =", -12)  # 単項マイナス
print("6 + 4 =", 6 + 4)  # 加算
print("6 - 4 =", 6 - 4)  # 減算
print("6 * 4 =", 6 * 4)  # 乗算
print("6 ** 4 =", 6 ** 4)  # べき乗
print("6 / 4 =", 6 / 4)  # 除算
print("6 // 4 =", 6 // 4)  # 切捨除算
print("6 % 4 =", 6 % 4)  # 剰余
