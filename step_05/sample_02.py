print("range(1, 12, 1)")
for i in range(1, 12, 1):  # 1から12まで1つずつ繰り返します
    print("変数iは", i, "です")

print("range(2, 10, 2)")
for i in range(2, 10, 2):  # 2から10まで2つずつ繰り返します
    print("変数iは", i, "です")

print("range(12, 1, -1)")
for i in range(12, 1, -1):  # 間隔にはマイナスの値を指定することもできます
    print("変数iは", i, "です")
